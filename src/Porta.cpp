#include "Porta.hpp"

/*=================================================================//
 * CONSTRUTOR                                         
//=================================================================*/
Porta::Porta()
{
    aberta = false;
}

/*=================================================================//
 * DESTRUTOR       
//=================================================================*/
Porta::~Porta()
{

}

/*=================================================================//
 * METODO: cond_abertura_porta
//=================================================================*/
bool Porta::abrir()
{
    aberta = true;
    return aberta;
}

/*=================================================================//
 * METODO: def_direcao
//=================================================================*/
bool Porta::fechar()
{
    aberta = false;
    return aberta;
}


